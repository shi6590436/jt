package com.net.jianshu.pojo;

public class Detail {
    private String id;
    private String title;
    private String content;

    public String getId() {
        return id;
    }

    public Detail setId(String id) {
        this.id = id;
        return this;
    }

    public String getTitle() {
        return title;
    }

    public Detail setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getContent() {
        return content;
    }

    public Detail setContent(String content) {
        this.content = content;
        return this;
    }
}
